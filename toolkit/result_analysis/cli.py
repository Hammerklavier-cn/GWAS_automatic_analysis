import os
import argparse

from enum import Enum, auto


class AnalysisOption(Enum):
    SNP_FREQUENCY_RANK = auto()
    SNP_PHENOTYPE_PAIR_RANK = auto()
    SNP_ETHNICITY_PAIR_RANK = auto()
    SNP_PHENOTYPE_DUPLICATION_RANK = auto()
    ALL = auto()
    pass


